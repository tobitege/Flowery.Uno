namespace Flowery.Controls
{
    /// <summary>
    /// Neumorphic visual effect modes.
    /// </summary>
    public enum DaisyNeumorphicMode
    {
        /// <summary>No neumorphic effect applied.</summary>
        None,

        /// <summary>Element appears raised/extruded from the surface.</summary>
        Raised,

        /// <summary>Element appears pressed/inset into the surface.</summary>
        Inset,

        /// <summary>Flat element with subtle shadow (single shadow, not dual).</summary>
        Flat
    }
}
