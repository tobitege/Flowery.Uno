        /// <summary>
        /// Retrieves a Geometry resource by key.
        /// Looks up local standard paths first (always returning a fresh instance) to avoid WinUI sharing/threading exceptions.
        /// Warning: If falling back to Application Resources for Geometry, it might return a shared instance which can cause exceptions if assigned to multiple Paths.
        /// </summary>
        public static Geometry? GetIconGeometry(string resourceKey)
        {
            // 1. Try standard paths first. This is safer as it creates a fresh Geometry each time.
            if (TryGetPathData(StandardIconPaths, resourceKey, out var pathData))
            {
               return ParsePathGeometry(pathData);
            }

            // 2. Fallback to resource lookup (risky if shared!)
            var resources = Application.Current?.Resources;
            if (resources == null)
                return null;

            if (DaisyResourceLookup.TryGetResource(resources, resourceKey, out var value) && value is Geometry geometry)
                return geometry;

            return null;
        }

        /// <summary>
        /// Creates a Path element from a brand icon key stored in StandardIconPaths.
        /// For multi-part logos (e.g., Google, Microsoft, Slack, MetaMask), call this per part.
        /// </summary>
        public static Path? CreateBrandIcon(string brandKey, double size = 16, Brush? fill = null, Brush? stroke = null,
            double strokeThickness = 0, Stretch stretch = Stretch.Uniform)
        {
            if (!TryGetPathData(StandardIconPaths, brandKey, out var pathData))
            {
                return null;
            }

            var path = new Path
            {
                Data = ParsePathGeometry(pathData),
                Width = size,
                Height = size,
                Stretch = stretch
            };

            if (strokeThickness > 0)
            {
                path.StrokeThickness = strokeThickness;
                path.Stroke = stroke ?? DaisyResourceLookup.GetBrush("DaisyBaseContentBrush");
                PlatformCompatibility.SafeSetRoundedStroke(path, setLineJoin: true);
            }

            if (fill != null || strokeThickness <= 0)
            {
                path.Fill = fill ?? DaisyResourceLookup.GetBrush("DaisyBaseContentBrush");
            }

            return path;
        }

