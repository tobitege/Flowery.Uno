        /// <summary>
        /// Creates a transform that normalizes an icon's original viewbox into a square target size.
        /// </summary>
        /// <param name="sourceWidth">Original SVG viewbox width.</param>
        /// <param name="sourceHeight">Original SVG viewbox height.</param>
        /// <param name="targetSize">Target square size (default: 96).</param>
        public static Transform CreateNormalizedTransform(double sourceWidth, double sourceHeight, double targetSize = 96)
        {
            var maxDimension = Math.Max(sourceWidth, sourceHeight);
            var scale = targetSize / maxDimension;
            var scaledWidth = sourceWidth * scale;
            var scaledHeight = sourceHeight * scale;
            var offsetX = (targetSize - scaledWidth) / 2d;
            var offsetY = (targetSize - scaledHeight) / 2d;

            var group = new TransformGroup();
            group.Children.Add(new ScaleTransform { ScaleX = scale, ScaleY = scale });
            group.Children.Add(new TranslateTransform { X = offsetX, Y = offsetY });
            return group;
        }

        /// <summary>
        /// Creates a deep clone of a Geometry to avoid shared-instance issues.
        /// WinUI/Uno Geometry objects can only be parented to one element at a time.
        /// This method creates a fresh copy that can be safely assigned to a new Path.
        /// </summary>
        public static Geometry? CloneGeometry(Geometry? source)
        {
            if (source == null)
                return null;

            switch (source)
            {
                case PathGeometry pg:
                    var clone = new PathGeometry { FillRule = pg.FillRule };
                    foreach (var figure in pg.Figures)
                    {
                        var clonedFigure = new PathFigure
                        {
                            StartPoint = figure.StartPoint,
                            IsClosed = figure.IsClosed,
                            IsFilled = figure.IsFilled
                        };
                        foreach (var segment in figure.Segments)
                        {
                            clonedFigure.Segments.Add(CloneSegment(segment));
                        }
                        clone.Figures.Add(clonedFigure);
                    }
                    return clone;

                case EllipseGeometry eg:
                    return new EllipseGeometry
                    {
                        Center = eg.Center,
                        RadiusX = eg.RadiusX,
                        RadiusY = eg.RadiusY
                    };

                case RectangleGeometry rg:
                    return new RectangleGeometry { Rect = rg.Rect };

                case LineGeometry lg:
                    return new LineGeometry
                    {
                        StartPoint = lg.StartPoint,
                        EndPoint = lg.EndPoint
                    };

                case GeometryGroup gg:
                    var groupClone = new GeometryGroup { FillRule = gg.FillRule };
                    foreach (var child in gg.Children)
                    {
                        var childClone = CloneGeometry(child);
                        if (childClone != null)
                            groupClone.Children.Add(childClone);
                    }
                    return groupClone;

                default:
                    // Fallback: return null; caller should handle with TrySetPathData pattern
                    System.Diagnostics.Debug.WriteLine($"[FloweryPathHelpers] CloneGeometry: Unsupported geometry type {source.GetType().Name}");
                    return null;
            }
        }

        private static PathSegment CloneSegment(PathSegment segment)
        {
            return segment switch
            {
                ArcSegment arc => new ArcSegment
                {
                    Point = arc.Point,
                    Size = arc.Size,
                    RotationAngle = arc.RotationAngle,
                    IsLargeArc = arc.IsLargeArc,
                    SweepDirection = arc.SweepDirection
                },
                BezierSegment bez => new BezierSegment
                {
                    Point1 = bez.Point1,
                    Point2 = bez.Point2,
                    Point3 = bez.Point3
                },
                LineSegment line => new LineSegment { Point = line.Point },
                QuadraticBezierSegment quad => new QuadraticBezierSegment
                {
                    Point1 = quad.Point1,
                    Point2 = quad.Point2
                },
                PolyLineSegment poly => ClonePolyLineSegment(poly),
                PolyBezierSegment polyBez => ClonePolyBezierSegment(polyBez),
                PolyQuadraticBezierSegment polyQuad => ClonePolyQuadraticBezierSegment(polyQuad),
                _ => new LineSegment { Point = new Windows.Foundation.Point(0, 0) }
            };
        }

        private static PolyLineSegment ClonePolyLineSegment(PolyLineSegment source)
        {
            var clone = new PolyLineSegment();
            foreach (var point in source.Points)
                clone.Points.Add(point);
            return clone;
        }

        private static PolyBezierSegment ClonePolyBezierSegment(PolyBezierSegment source)
        {
            var clone = new PolyBezierSegment();
            foreach (var point in source.Points)
                clone.Points.Add(point);
            return clone;
        }

        private static PolyQuadraticBezierSegment ClonePolyQuadraticBezierSegment(PolyQuadraticBezierSegment source)
        {
            var clone = new PolyQuadraticBezierSegment();
            foreach (var point in source.Points)
                clone.Points.Add(point);
            return clone;
        }

        public static void TrySetPathData(Path? path, Func<Geometry> geometryFactory, Func<Geometry>? fallbackFactory = null)
        {
            if (path == null)
                return;

            try
            {
                path.Data = geometryFactory();
            }
            catch (Exception ex) when (ex is ArgumentException or InvalidCastException or COMException)
            {
                System.Diagnostics.Debug.WriteLine($"[FloweryPathHelpers] Failed to set primary path data: {ex.Message}");

                if (fallbackFactory == null)
                    return;

                try
                {
                    path.Data = fallbackFactory();
                }
                catch (Exception fallbackEx)
                {
                    System.Diagnostics.Debug.WriteLine($"[FloweryPathHelpers] Failed to set fallback path data: {fallbackEx.Message}");
                    // Intentionally ignore: keep the control alive even if icon data fails.
                }
            }
        }

        public static Geometry CreateEllipseGeometry(Point center, double radiusX, double radiusY)
        {
            if (radiusX <= 0 || radiusY <= 0)
            {
                return new PathGeometry();
            }

            var startPoint = new Point(center.X + radiusX, center.Y);
            var figure = new PathFigure
            {
                StartPoint = startPoint,
                IsClosed = true,
                IsFilled = true
            };

            figure.Segments.Add(new ArcSegment
            {
                Point = new Point(center.X - radiusX, center.Y),
                Size = new Size(radiusX, radiusY),
                RotationAngle = 0,
                IsLargeArc = false,
                SweepDirection = SweepDirection.Clockwise
            });

            figure.Segments.Add(new ArcSegment
            {
                Point = startPoint,
                Size = new Size(radiusX, radiusY),
                RotationAngle = 0,
                IsLargeArc = false,
                SweepDirection = SweepDirection.Clockwise
            });

            var geometry = new PathGeometry();
            geometry.Figures.Add(figure);
            return geometry;
        }

        public static Geometry CreateEllipseGeometry(double centerX, double centerY, double radiusX, double radiusY)
        {
            return CreateEllipseGeometry(new Point(centerX, centerY), radiusX, radiusY);
        }

        private static Geometry CreateFallbackWeatherGeometry()
        {
            return CreateEllipseGeometry(12, 12, 10, 10);
        }
