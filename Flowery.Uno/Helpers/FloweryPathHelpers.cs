        public static Geometry ParsePathGeometry(string data)
        {
            // Use XamlBindingHelper to parse the string path data into a Geometry.
            // This is the standard WinUI way to parse path data and avoids manual regex errors.
            try
            {
                return (Geometry)Microsoft.UI.Xaml.Markup.XamlBindingHelper.ConvertValue(typeof(Geometry), data);
            }
            catch
            {
                return CreateFallbackWeatherGeometry();
            }
        }

